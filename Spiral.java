import java.util.*;
public class Spiral {
    public static void main(String[] args) {
        int n;
        Scanner sc=new Scanner(System.in);
        n=sc.nextInt();
        int ar[][]=new int[n][n];
        for(int i=0;i<ar.length;i++)
        {
            for(int j=0;j<ar.length;j++)
            ar[i][j]=sc.nextInt();
        }
    }
    
    
}
